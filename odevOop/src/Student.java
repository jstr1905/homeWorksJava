
public class Student {

	public Student() {

	}

	public Student(int id, String name, String surname, String email, int age) {

		this.id = id;
		this.name = name;
		this.surname = surname;
		this.email = email;
		this.age = age;

	}

	int id;
	String name;
	String surname;
	String email;
	int age;

}
